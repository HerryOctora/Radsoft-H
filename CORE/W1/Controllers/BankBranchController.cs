        /*
         * param1 = userID
         * param2 = sessionID
         * param3 = permissionID
         */
        [HttpPost]
        public HttpResponseMessage Action(string param1, string param2, string param3, [FromBody]BankBranch _bankBranch)
        {
            string PermissionID;
            PermissionID = param3;
            try
            {
                bool session = Tools.SessionCheck(param1, param2);
                if (session)
                {
                    try
                    {
                        bool havePermission = _host.Get_Permission(param1, PermissionID);
                        if (havePermission)
                        {
                            bool havePrivillege = _host.Get_Privillege(param1, PermissionID);
                            if (PermissionID == "BankBranch_U")
                            {
                                int _newHisPK = _bankBranchReps.BankBranch_Update(_bankBranch, havePrivillege);

                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Update Bank Branch Success", _Obj, "", param1, _bankBranch.BankBranchPK, _bankBranch.HistoryPK, _newHisPK, "UPDATE");
                                return Request.CreateResponse(HttpStatusCode.OK, "Update Bank Branch Success");
                            }
                            if (PermissionID == "BankBranch_A")
                            {
                                _bankBranchReps.BankBranch_Approved(_bankBranch);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Approved Bank Branch Success", _Obj, "", param1, _bankBranch.BankBranchPK, _bankBranch.HistoryPK, 0, "APPROVED");
                                return Request.CreateResponse(HttpStatusCode.OK, "Approved Bank Branch Success");
                            }
                            if (PermissionID == "BankBranch_V")
                            {
                                _bankBranchReps.BankBranch_Void(_bankBranch);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Void Bank Branch Success", _Obj, "", param1, _bankBranch.BankBranchPK, _bankBranch.HistoryPK, 0, "VOID");
                                return Request.CreateResponse(HttpStatusCode.OK, "Void Bank Branch Success");
                            }
                            if (PermissionID == "BankBranch_R")
                            {
                                _bankBranchReps.BankBranch_Reject(_bankBranch);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Reject Bank Branch Success", _Obj, "", param1, _bankBranch.BankBranchPK, _bankBranch.HistoryPK, 0, "REJECT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Reject Bank Branch Success");
                            }
                            if (PermissionID == "BankBranch_I")
                            {
                                int _lastPKByLastUpdate = _bankBranchReps.BankBranch_Add(_bankBranch, havePrivillege);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Add Bank Branch Success", _Obj, "", param1, _lastPKByLastUpdate, 0, 0, "INSERT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Insert Bank Branch Success");
                            }
                            else
                            {
                                return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoActionMessage);
                            }

                        }
                        else
                        {
                            _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoPermissionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                            return Request.CreateResponse(HttpStatusCode.BadRequest, Tools.NoPermissionMessage);
                        }
                    }
                    catch (Exception err)
                    {
                        throw err;
                    }
                }
                else
                {
                    _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoSessionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                    return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoSessionMessage);
                }
            }
            catch (Exception err)
            {
                _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.InternalErrorMessage, err.StackTrace, err.Message, param1, 0, 0, 0, "");
                if (Tools.ClientCode == "05") { return Request.CreateResponse(HttpStatusCode.InternalServerError, "Please Contact Administrator"); } else { return Request.CreateResponse(HttpStatusCode.InternalServerError, Tools.ErrorPrefix + err.Message); }
            }
        }


