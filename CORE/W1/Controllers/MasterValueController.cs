        /*
         * param1 = userID
         * param2 = sessionID
         * param3 = permissionID
         */
        [HttpPost]
        public HttpResponseMessage Action(string param1, string param2, string param3, [FromBody]MasterValue _masterValue)
        {
            string PermissionID;
            PermissionID = param3;
            try
            {
                bool session = Tools.SessionCheck(param1, param2);
                if (session)
                {
                    try
                    {
                        bool havePermission = _host.Get_Permission(param1, PermissionID);
                        if (havePermission)
                        {
                            bool havePrivillege = _host.Get_Privillege(param1, PermissionID);
                            if (PermissionID == "MasterValue_U")
                            {
                                int _newHisPK = _masterValueReps.MasterValue_Update(_masterValue, havePrivillege);

                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Update Master Value Success", _Obj, "", param1, _masterValue.MasterValuePK, _masterValue.HistoryPK, _newHisPK, "UPDATE");
                                return Request.CreateResponse(HttpStatusCode.OK, "Update Master Value Success");
                            }
                            if (PermissionID == "MasterValue_A")
                            {
                                _masterValueReps.MasterValue_Approved(_masterValue);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Approved Master Value Success", _Obj, "", param1, _masterValue.MasterValuePK, _masterValue.HistoryPK, 0, "APPROVED");
                                return Request.CreateResponse(HttpStatusCode.OK, "Approved Master Value Success");
                            }
                            if (PermissionID == "MasterValue_V")
                            {
                                _masterValueReps.MasterValue_Void(_masterValue);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Void Master Value Success", _Obj, "", param1, _masterValue.MasterValuePK, _masterValue.HistoryPK, 0, "VOID");
                                return Request.CreateResponse(HttpStatusCode.OK, "Void Master Value Success");
                            }
                            if (PermissionID == "MasterValue_R")
                            {
                                _masterValueReps.MasterValue_Reject(_masterValue);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Reject Master Value Success", _Obj, "", param1, _masterValue.MasterValuePK, _masterValue.HistoryPK, 0, "REJECT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Reject Master Value Success");
                            }
                            if (PermissionID == "MasterValue_I")
                            {
                                int _lastPK = _masterValueReps.MasterValue_Add(_masterValue, havePrivillege);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Add Master Value Success", _Obj, "", param1, _lastPK, 0, 0, "INSERT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Insert Master Value Success");
                            }
                            else
                            {
                                return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoActionMessage);
                            }

                        }
                        else
                        {
                            _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoPermissionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                            return Request.CreateResponse(HttpStatusCode.BadRequest, Tools.NoPermissionMessage);
                        }
                    }
                    catch (Exception err)
                    {
                        throw err;
                    }
                }
                else
                {
                    _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoSessionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                    return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoSessionMessage);
                }
            }
            catch (Exception err)
            {
                _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.InternalErrorMessage, err.StackTrace, err.Message, param1, 0, 0, 0, "");
                if (Tools.ClientCode == "05") { return Request.CreateResponse(HttpStatusCode.InternalServerError, "Please Contact Administrator"); } else { return Request.CreateResponse(HttpStatusCode.InternalServerError, Tools.ErrorPrefix + err.Message); }
            }
        }
