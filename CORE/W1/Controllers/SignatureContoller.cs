        /*
         * param1 = userID
         * param2 = sessionID
         * param3 = permissionID
         */
        [HttpPost]
        public HttpResponseMessage Action(string param1, string param2, string param3, [FromBody]Signature _signature)
        {
            string PermissionID;
            PermissionID = param3;
            try
            {
                bool session = Tools.SessionCheck(param1, param2);
                if (session)
                {
                    try
                    {
                        bool havePermission = _host.Get_Permission(param1, PermissionID);
                        if (havePermission)
                        {
                            bool havePrivillege = _host.Get_Privillege(param1, PermissionID);
                            if (PermissionID == "Signature_U")
                            {
                                int _newHisPK = _signatureReps.Signature_Update(_signature, havePrivillege);

                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Update Signature Success", _Obj, "", param1, _signature.SignaturePK, _signature.HistoryPK, _newHisPK, "UPDATE");
                                return Request.CreateResponse(HttpStatusCode.OK, "Update Signature Success");
                            }
                            if (PermissionID == "Signature_A")
                            {
                                _signatureReps.Signature_Approved(_signature);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Approved Signature Success", _Obj, "", param1, _signature.SignaturePK, _signature.HistoryPK, 0, "APPROVED");
                                return Request.CreateResponse(HttpStatusCode.OK, "Approved Signature Success");
                            }
                            if (PermissionID == "Signature_V")
                            {
                                _signatureReps.Signature_Void(_signature);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Void Signature Success", _Obj, "", param1, _signature.SignaturePK, _signature.HistoryPK, 0, "VOID");
                                return Request.CreateResponse(HttpStatusCode.OK, "Void Signature Success");
                            }
                            if (PermissionID == "Signature_R")
                            {
                                _signatureReps.Signature_Reject(_signature);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Reject Signature Success", _Obj, "", param1, _signature.SignaturePK, _signature.HistoryPK, 0, "REJECT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Reject Signature Success");
                            }
                            if (PermissionID == "Signature_I")
                            {
                                int _lastPKByLastUpdate = _signatureReps.Signature_Add(_signature, havePrivillege);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Add Signature Success", _Obj, "", param1, _lastPKByLastUpdate, 0, 0, "INSERT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Insert Signature Success");
                            }
                            else
                            {
                                return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoActionMessage);
                            }
                        }
                        else
                        {
                            _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoPermissionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                            return Request.CreateResponse(HttpStatusCode.BadRequest, Tools.NoPermissionMessage);
                        }
                    }
                    catch (Exception err)
                    {
                        throw err;
                    }
                }
                else
                {
                    _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoSessionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                    return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoSessionMessage);
                }
            }
            catch (Exception err)
            {
                _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.InternalErrorMessage, err.StackTrace, err.Message, param1, 0, 0, 0, "");
                if (Tools.ClientCode == "05") { return Request.CreateResponse(HttpStatusCode.InternalServerError, "Please Contact Administrator"); } else { return Request.CreateResponse(HttpStatusCode.InternalServerError, Tools.ErrorPrefix + err.Message); }
            }
        }


