        /*
         * param1 = userID
         * param2 = sessionID
         * param3 = permissionID
         */
        [HttpPost]
        public HttpResponseMessage Action(string param1, string param2, string param3, [FromBody]Counterpart _counterpart)
        {
            string PermissionID;
            PermissionID = param3;
            try
            {
                bool session = Tools.SessionCheck(param1, param2);
                if (session)
                {
                    try
                    {
                        bool havePermission = _host.Get_Permission(param1, PermissionID);
                        if (havePermission)
                        {
                            bool havePrivillege = _host.Get_Privillege(param1, PermissionID);
                            if (PermissionID == "Counterpart_U")
                            {
                                int _newHisPK = _counterpartReps.Counterpart_Update(_counterpart, havePrivillege);

                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Update Counterpart Success", _Obj, "", param1, _counterpart.CounterpartPK, _counterpart.HistoryPK, _newHisPK, "UPDATE");
                                return Request.CreateResponse(HttpStatusCode.OK, "Update Counterpart Success");
                            }
                            if (PermissionID == "Counterpart_A")
                            {
                                _counterpartReps.Counterpart_Approved(_counterpart);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Approved Roles Success", _Obj, "", param1, _counterpart.CounterpartPK, _counterpart.HistoryPK, 0, "APPROVED");
                                return Request.CreateResponse(HttpStatusCode.OK, "Approved Counterpart Success");
                            }
                            if (PermissionID == "Counterpart_V")
                            {
                                _counterpartReps.Counterpart_Void(_counterpart);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Void Counterpart Success", _Obj, "", param1, _counterpart.CounterpartPK, _counterpart.HistoryPK, 0, "VOID");
                                return Request.CreateResponse(HttpStatusCode.OK, "Void Counterpart Success");
                            }
                            if (PermissionID == "Counterpart_R")
                            {
                                _counterpartReps.Counterpart_Reject(_counterpart);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Reject Counterpart Success", _Obj, "", param1, _counterpart.CounterpartPK, _counterpart.HistoryPK, 0, "REJECT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Reject Counterpart Success");
                            }
                            if (PermissionID == "Counterpart_I")
                            {
                                int _lastPKByLastUpdate = _counterpartReps.Counterpart_Add(_counterpart, havePrivillege);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Add Counterpart Success", _Obj, "", param1, _lastPKByLastUpdate, 0, 0, "INSERT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Insert Counterpart Success");
                            }
                            if (PermissionID == "Counterpart_AddCounterpartFeeSetup")
                            {
                                int _lastPK = _counterpartReps.AddCounterpartFee(_counterpart, havePrivillege);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Add Counterpart Fee Success", _Obj, "", param1, _lastPK, 0, 0, "INSERT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Insert Counterpart Fee Success");
                            }
                            else
                            {
                                return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoActionMessage);
                            }

                        }
                        else
                        {
                            _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoPermissionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                            return Request.CreateResponse(HttpStatusCode.BadRequest, Tools.NoPermissionMessage);
                        }
                    }
                    catch (Exception err)
                    {
                        throw err;
                    }
                }
                else
                {
                    _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoSessionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                    return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoSessionMessage);
                }
            }
            catch (Exception err)
            {
                _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.InternalErrorMessage, err.StackTrace, err.Message, param1, 0, 0, 0, "");
                if (Tools.ClientCode == "05") { return Request.CreateResponse(HttpStatusCode.InternalServerError, "Please Contact Administrator"); } else { return Request.CreateResponse(HttpStatusCode.InternalServerError, Tools.ErrorPrefix + err.Message); }
            }
        }

