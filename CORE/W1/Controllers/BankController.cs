        /*
         * param1 = userID
         * param2 = sessionID
         * param3 = permissionID
         */
        [HttpPost]
        public HttpResponseMessage Action(string param1, string param2, string param3, [FromBody]Bank _bank)
        {
            string PermissionID;
            PermissionID = param3;
            try
            {
                bool session = Tools.SessionCheck(param1, param2);
                if (session)
                {
                    try
                    {
                        bool havePermission = _host.Get_Permission(param1, PermissionID);
                        if (havePermission)
                        {
                            bool havePrivillege = _host.Get_Privillege(param1, PermissionID);
                            if (PermissionID == "Bank_U")
                            {
                                int _newHisPK = _bankReps.Bank_Update(_bank, havePrivillege);

                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Update Bank Custodian Success", _Obj, "", param1, _bank.BankPK, _bank.HistoryPK, _newHisPK, "UPDATE");
                                return Request.CreateResponse(HttpStatusCode.OK, "Update Bank Custodian Success");
                            }
                            if (PermissionID == "Bank_A")
                            {
                                _bankReps.Bank_Approved(_bank);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Approved Bank Custodian Success", _Obj, "", param1, _bank.BankPK, _bank.HistoryPK, 0, "APPROVED");
                                return Request.CreateResponse(HttpStatusCode.OK, "Approved Bank Custodian Success");
                            }
                            if (PermissionID == "Bank_V")
                            {
                                _bankReps.Bank_Void(_bank);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Void Bank Custodian Success", _Obj, "", param1, _bank.BankPK, _bank.HistoryPK, 0, "VOID");
                                return Request.CreateResponse(HttpStatusCode.OK, "Void Bank Custodian Success");
                            }
                            if (PermissionID == "Bank_R")
                            {
                                _bankReps.Bank_Reject(_bank);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Reject Bank Custodian Success", _Obj, "", param1, _bank.BankPK, _bank.HistoryPK, 0, "REJECT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Reject Bank Custodian Success");
                            }
                            if (PermissionID == "Bank_I")
                            {
                                int _lastPKByLastUpdate = _bankReps.Bank_Add(_bank, havePrivillege);
                                _activityReps.Activity_LogInsert(DateTime.Now, PermissionID, true, "Add Bank Custodian Success", _Obj, "", param1, _lastPKByLastUpdate, 0, 0, "INSERT");
                                return Request.CreateResponse(HttpStatusCode.OK, "Insert Bank Custodian Success");
                            }
                            else
                            {
                                return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoActionMessage);
                            }

                        }
                        else
                        {
                            _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoPermissionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                            return Request.CreateResponse(HttpStatusCode.BadRequest, Tools.NoPermissionMessage);
                        }
                    }
                    catch (Exception err)
                    {
                        throw err;
                    }
                }
                else
                {
                    _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.NoSessionLogMessage, _Obj, "Action", param1, 0, 0, 0, "");
                    return Request.CreateResponse(HttpStatusCode.NotFound, Tools.NoSessionMessage);
                }
            }
            catch (Exception err)
            {
                _activityReps.Activity_LogInsert(DateTime.Now, "", false, Tools.InternalErrorMessage, err.StackTrace, err.Message, param1, 0, 0, 0, "");
                if (Tools.ClientCode == "05") { return Request.CreateResponse(HttpStatusCode.InternalServerError, "Please Contact Administrator"); } else { return Request.CreateResponse(HttpStatusCode.InternalServerError, Tools.ErrorPrefix + err.Message); }
            }
        }

